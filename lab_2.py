import numpy as np
import matplotlib.pyplot as plt
f=20
t=np.linspace(0,10,100)
x1=np.sin(2*np.pi*f*t)
plt.figure(figsize=(10, 4))
plt.subplot(1, 2, 1)
plt.plot(t,x1)
plt.subplot(1, 2, 2)
plt.stem(t,x1)
plt.show()
